'''
Tipo Float

Tipo real, decimal

Casas decimais

o separador de casas decimais na programação é o . e não a virgula.
'''

valor = 1.44
# errado valor = 1,44
valor2 = 1, 44

print(valor)
print(valor2) # aqui ele irá falar que é uma tupla 
print(type(valor2))

# É possível fazer dupla atribuição

valor3, valor4 = 1, 44

print(valor3)
print(valor4)


# Podemos trabalhar com números complexos

variavel = 5j
print(variavel)
print(type(variavel))

